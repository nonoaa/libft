#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*a;

	i = 0;
	a = b;
	while (len > 0)
	{
		a[i] = c;
		i++;
		len--;
	}
	return (b);
}
