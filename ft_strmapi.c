#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*out;
	unsigned int	i;
	unsigned int	len;

	if (!s)
		return (0);
	i = 0;
	len = ft_strlen(s);
	if (!(out = (char *)malloc(sizeof(char) * (len + 1))))
		return (0);
	while (i < len)
	{
		out[i] = f(i, s[i]);
		i++;
	}
	out[i] = '\0';
	return (out);
}
